void	ft_rev_int_tab(int *tab, int size)
{
	int	n;
	int	temp;

	n = 0;
	while (n < size / 2)
	{
		temp = tab[size - 1 - n];
		tab[size - 1 - n] = tab[n];
		tab[n] = temp;
		n++;
	}
}
