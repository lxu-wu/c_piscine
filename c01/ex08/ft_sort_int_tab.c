void	ft_sort_int_tab(int *tab, int size)
{
	int	n;
	int	stock;

	n = 0;
	while (n < size - 1)
	{
		if (tab[n] > tab[n + 1])
		{
			stock = tab[n];
			tab[n] = tab[n + 1];
			tab[n + 1] = stock;
			n = 0;
		}
		else
		{
			n++;
		}
	}
}
