#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	neg;

	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	i = 0;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			i++;
		str++;
	}
	neg = i;
	i = 0;
	while ((*str >= '0' && *str <= '9'))
	{
		i = i * 10 + *str - '0';
		str++;
	}
	if (neg % 2 == 1)
		return (-i);
	return (i);
}
