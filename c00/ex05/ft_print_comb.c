#include <unistd.h>

void	ft_print_comb(void)
{
	int	t[3];

	t[0] = -1 + '0';
	while (t[0]++ < '7')
	{
		t[1] = t[0];
		while (t[1]++ < '8')
		{
			t[2] = t[1];
			{
				while (t[2]++ < '9')
				{
					if (!(t[0] == 48 && t[1] == 49 && t[2] == 50))
						write(1, ", ", 2);
					write(1, &t[0], 1);
					write(1, &t[1], 1);
					write(1, &t[2], 1);
				}
			}
		}
	}
}
